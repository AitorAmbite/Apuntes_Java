public class OperacionesVariables {

    public static void main(String[] args) {

        /*
        * Operaciones con variables.
        * Como se ha mencionado anteriormente, se pueden realizar operaciones con las variables.
        *
        * BASICAS:
        * SUMA  : a + b
        * RESTA : a - b
        * MULTIPLICACION : a * b
        * DIVISION : a / b
        * RESTO : a % b
        *
        *
        * SHORCUTS - ATAJOS
        * a++ : suma 1 a la variable, evalua primero y suma despues
        * ++a : suma 1 a la variable, suma primero y despues evalua
        * a-- : resta 1 a la variable, evalua primero y suma despues
        * --a : resta 1 a la variable, suma primero y despues evalua
        * */

        int a = 10;
        int b = 20;

        int Suma = a + b; // esto asigna a la variable Suma, el resultado de sumar las dos variables
        int resta = a - b; // esto asigna a la variable Resta, el resultado de restar las dos variables
        int division = a / b;  // esto asigna a la variable division, el resultado de dividir las dos variables
        int resto = a % b; // esto asigna a la variable resto, el resto de la division las dos variables
        /*
        * Se pueden hacer tantas operaciones como quieras con las variables.
        * */
    }
}
